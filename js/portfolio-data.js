// Helper function to get localized portfolio data
function getLocalizedPortfolioData(id, lang = null) {
    const currentLang = lang || localStorage.getItem('language') || 'fr';
    const portfolio = portfolioDataMultilang[id];
    
    if (!portfolio) return null;
    
    // Create a localized version
    const localized = {
        id: id,
        image: portfolio.image,
        images: portfolio.images,
        client: portfolio.client,
        consultant: portfolio.consultant,
        promoteur: typeof portfolio.promoteur === 'object' ? portfolio.promoteur[currentLang] : portfolio.promoteur,
        responsable: portfolio.responsable,
        contact: portfolio.contact,
        website: portfolio.website,
        date: portfolio.date,
        relatedProjects: portfolio.relatedProjects
    };
    
    // Handle multilingual fields
    const multilingualFields = ['title', 'category', 'description', 'fullDescription', 'beneficiaire', 
                                'financement', 'personnelCle', 'pays', 'dateDebut', 'dateFin'];
    
    multilingualFields.forEach(field => {
        if (portfolio[field]) {
            localized[field] = typeof portfolio[field] === 'object' ? 
                              (portfolio[field][currentLang] || portfolio[field]['fr']) : 
                              portfolio[field];
        }
    });
    
    return localized;
}

// Function to get all portfolio items in current language
function getAllPortfolioData(lang = null) {
    const currentLang = lang || localStorage.getItem('language') || 'fr';
    const result = {};
    
    Object.keys(portfolioDataMultilang).forEach(id => {
        result[id] = getLocalizedPortfolioData(id, currentLang);
    });
    
    return result;
}

// For backward compatibility, keep the original functions
function getPortfolioItem(id) {
    return getLocalizedPortfolioData(id);
}

function getRelatedProjects(projectId) {
    const project = getLocalizedPortfolioData(projectId);
    if (!project || !project.relatedProjects) return [];
    
    return project.relatedProjects.map(id => ({
        id: id,
        image: portfolioDataMultilang[id].image,
        title: getLocalizedPortfolioData(id).title
    }));
}
